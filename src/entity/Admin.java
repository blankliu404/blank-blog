public class Admin {
	/*
	 * 管理员实例
	 * @Param
	 * admin 管理员名
	 * password 管理员密码
	 * */
	private String admin;
	private String password;
	public String getAdmin() {
		return admin;
	}
	public void setAdmin(String admin) {
		this.admin = admin;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
	
}
